    class masakan extends MenuMasakanAdm{
        int id_masakan, harga;
        String nama_masakan, status;
        
        public masakan(){
            this.nama_masakan = jTextField2 .getText();
            this.harga = Integer.parseInt(jTextField3.getText());
            this.status = jComboBox1.getSelectedItem().toString();
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Masakan");
        model.addColumn("Nama Masakan");
        model.addColumn("Harga");
        model.addColumn("Status");
        jTable1.setModel(model);
        try{
            this.stat = k.getCon().prepareStatement("select *from masakan");
            this.rs = this.stat.executeQuery();
            while (rs.next()){
                Object[] data={
                rs.getInt("id_masakan"),
                rs.getString("nama_masakan"),
                rs.getInt("harga"),
                rs.getString("status")
            };
                model.addRow(data);
            };
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        
    }
    
